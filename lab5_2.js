// 2. Arrow function
var add = function (a, b) {
    return a + b;
};
var sub = function (a, b) {
    return a - b;
};
console.log("Addition :", add(1, 3)); // Output: Addition : 4
console.log("Subtraction :", sub(5, 1)); // Output: Subtraction : 4
